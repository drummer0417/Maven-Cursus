package nl.androidappfactory.util;

public class Math {

    public static int add(int a, int b) {

        return a + b;
    }

    public static int substract(int a, int b) {

        return a - b;
    }

    public static int multiply(int a, int b) {

        return a * b;
    }

    public static double devide(int a, int b) {

        return a / b;
    }
}
